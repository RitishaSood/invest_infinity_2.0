"""
evaluation/visualization.py

Generates and saves matplotlib plots for the ML pipeline:
- Training curves (loss/val_loss from train.py's history JSON)
- Actual vs predicted prices (from backtesting.py)
- Future forecast plots (from predict.py)

All plots are saved as PNG files inside config.plots_dir.
"""

import json
from pathlib import Path
from typing import List, Literal

import matplotlib

matplotlib.use("Agg")  # No display available in this environment - just save files
import matplotlib.pyplot as plt

from config.config import config
from utils.logger import get_logger

logger = get_logger(__name__)

ModelType = Literal["lstm", "gru"]


def plot_training_history(ticker: str, model_type: ModelType) -> Path:
    """Plot training vs validation loss curves from a saved history JSON."""
    model_dir = config.lstm_model_dir if model_type == "lstm" else config.gru_model_dir
    history_path = model_dir / f"{ticker}_{model_type}_history.json"

    with open(history_path, "r") as file:
        history = json.load(file)

    plt.figure(figsize=(8, 5))
    plt.plot(history["loss"], label="Training Loss")
    plt.plot(history["val_loss"], label="Validation Loss")
    plt.title(f"{ticker} {model_type.upper()} - Training History")
    plt.xlabel("Epoch")
    plt.ylabel("Loss (MSE)")
    plt.legend()

    output_path = config.plots_dir / f"{ticker}_{model_type}_training_history.png"
    plt.savefig(output_path)
    plt.close()

    logger.info(f"Saved training history plot to {output_path}")
    return output_path


def plot_actual_vs_predicted(
    ticker: str, model_type: ModelType, actual: List[float], predicted: List[float]
) -> Path:
    """Plot actual vs predicted prices, typically from a backtest result."""
    plt.figure(figsize=(10, 5))
    plt.plot(actual, label="Actual Price")
    plt.plot(predicted, label="Predicted Price")
    plt.title(f"{ticker} {model_type.upper()} - Actual vs Predicted")
    plt.xlabel("Time Step")
    plt.ylabel("Price ($)")
    plt.legend()

    output_path = config.plots_dir / f"{ticker}_{model_type}_actual_vs_predicted.png"
    plt.savefig(output_path)
    plt.close()

    logger.info(f"Saved actual vs predicted plot to {output_path}")
    return output_path


def plot_forecast(ticker: str, model_type: ModelType, forecast: List[float]) -> Path:
    """Plot a future price forecast generated by predict.py."""
    plt.figure(figsize=(10, 5))
    plt.plot(forecast, label="Forecasted Price", color="orange")
    plt.title(f"{ticker} {model_type.upper()} - {len(forecast)}-Day Forecast")
    plt.xlabel("Days Ahead")
    plt.ylabel("Price ($)")
    plt.legend()

    output_path = config.plots_dir / f"{ticker}_{model_type}_forecast.png"
    plt.savefig(output_path)
    plt.close()

    logger.info(f"Saved forecast plot to {output_path}")
    return output_path